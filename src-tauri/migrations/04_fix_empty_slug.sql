-- забыл заполнить поле в «+ Добавить пост». Это блокирует Гендиру dispatch_task
-- (нет slug → пост не найдётся) и в целом ломает audit trail.
--
-- Этот UPDATE идемпотентен: если slug уже корректный или поста нет —
-- ничего не делается. По умолчанию переносим в Техническое Отделение (#4),
-- т.к. frontend это технический пост по смыслу.
